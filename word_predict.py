def train_epoch(model, train_loader, optimizer, criterion, epoch):
    """Train for one epoch."""
    model.train()
    total_loss = 0.0
    correct = 0
    total = 0

    progress_bar = tqdm(train_loader, desc=f"Training Epoch {epoch+1}")

    for batch in progress_bar:
        neural_seq = batch['neural_sequence'].to(device)
        labels = batch['label'].to(device)
        words = batch['word']

        # Forward pass
        logits = model(neural_seq, words)
        loss = criterion(logits, labels)

        # Backward pass
        optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()

        # Calculate accuracy
        _, predicted = torch.max(logits.data, 1)
        total += labels.size(0)
        correct += (predicted == labels).sum().item()

        total_loss += loss.item()
        accuracy = 100 * correct / total

        progress_bar.set_postfix({
            'loss': f'{loss.item():.4f}',
            'acc': f'{accuracy:.2f}%'
        })

    return total_loss / len(train_loader), accuracy

def evaluate(model, val_loader, criterion, target_words):
    """Evaluate model."""
    model.eval()

    # --- FIX: Handle the case where the dataloader is empty ---
    if not val_loader or len(val_loader.dataset) == 0:
        print("‚ö†Ô∏è Warning: Evaluation loader is empty. Skipping evaluation.")
        return {
            'loss': 0.0,
            'accuracy': 0.0,
            'predictions': [],
            'labels': [],
            'words': [],
            'analysis': [],
            'classification_report': {},
            'confusion_matrix': np.array([])
        }
    # --- END FIX ---

    total_loss = 0.0
    all_predictions = []
    all_labels = []
    all_words = []
    all_analysis = []

    with torch.no_grad():
        for batch in tqdm(val_loader, desc="Evaluating"):
            neural_seq = batch['neural_sequence'].to(device)
            labels = batch['label'].to(device)
            words = batch['word']

            logits, analysis = model(neural_seq, words, return_analysis=True)
            loss = criterion(logits, labels)

            total_loss += loss.item()

            _, predicted = torch.max(logits.data, 1)
            all_predictions.extend(predicted.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())
            all_words.extend(words)
            all_analysis.append(analysis)

    # Calculate metrics
    accuracy = accuracy_score(all_labels, all_predictions)

    # Classification report
    unique_labels = np.unique(all_labels)
    existing_target_words = [target_words[i] for i in unique_labels]

    report = classification_report(all_labels, all_predictions,
                                 target_names=existing_target_words,
                                 labels=unique_labels,
                                 output_dict=True, zero_division=0)

    # Confusion matrix
    cm = confusion_matrix(all_labels, all_predictions)

    return {
        'loss': total_loss / len(val_loader),
        'accuracy': accuracy,
        'predictions': all_predictions,
        'labels': all_labels,
        'words': all_words,
        'analysis': all_analysis,
        'classification_report': report,
        'confusion_matrix': cm
    }

